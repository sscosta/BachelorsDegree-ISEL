example( N) :-
  example( N, O, _, I, _),
  statistics( runtime, _),
    bb_inf( I, O, Inf, _, 0.001),
    print( N:Inf), nl,
  statistics( runtime, [_,T]),
  print_time( T).

print_time( T) :- print_time( '%%% Timing', T).

%
% milliseconds
%
print_time( What, T) :-
        Seconds is T/1000,
        Hour is T // 3600000, 
        R0 is T-Hour*3600000,
        Min is R0 // 60000,
        R1 is R0-Min*60000,
        Sec is R1/1000,
        format( "~|~w~t~15+ ~|~`0t~d~2+:~|~`0t~d~2+:~|~`0t~3f~6+ ~t~3f~10+~n", 
                [What,Hour,Min,Sec,Seconds]).


