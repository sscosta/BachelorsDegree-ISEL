package com.isel.si1.Trabalho.model;

public class Acredita {


    private Integer processo;
    private Integer intituicao;
    private String curso;


    public Integer getProcesso() {
        return processo;
    }

    public void setProcesso(Integer processo) {
        this.processo = processo;
    }

    public Integer getIntituicao() {
        return intituicao;
    }

    public void setIntituicao(Integer intituicao) {
        this.intituicao = intituicao;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }
}
